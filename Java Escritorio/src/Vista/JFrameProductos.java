public void cargarTabla(){
        try {
            //Rescatamos la información de todos los productos de la BD
            ProductoDao dao = new ProductoDao();
            ResultSet rs = dao.obtener_productos();

            //Cargar una lista con la información de la BD:
            List<Producto> lista_productos = new ArrayList<>();
            while ( rs.next() ) {
                Producto p = new Producto();
                p.setId_producto( rs.getInt(1) );
                p.setNro_serie( rs.getString(2) );
                p.setCodigo( rs.getString(3) );
                p.setPrecio( rs.getInt(4) );
                p.setStock( rs.getInt(5) );
                p.setNombre( rs.getString(6) );

                p.setFecha_ingreso( rs.getDate(9) );

                lista_productos.add(p);
            }

            //cargar la tabla:
            String[][] arreglo = new String[lista_productos.size()][9];
            for (int i = 0; i < lista_productos.size(); i++) {
                arreglo[i][0] = lista_productos.get(i).getId_producto()+"";
                arreglo[i][1] = lista_productos.get(i).getNro_serie();
                arreglo[i][2] = lista_productos.get(i).getCodigo();
                arreglo[i][3] = lista_productos.get(i).getNombre();
                arreglo[i][4] = lista_productos.get(i).getPrecio()+"";
                arreglo[i][5] = lista_productos.get(i).getStock()+"";
                arreglo[i][6] = lista_productos.get(i).getFecha_ingreso()+"";
            }
            jTable1.setModel(new javax.swing.table.DefaultTableModel(
                arreglo,
                new String [] {
                    "ID_PRODUCTO", "NRO SERIE", "CODIGO","NOMBRE", "PRECIO","STOCK","FECHA INGRESO"
                }
            ) {
                Class[] types = new Class [] {
                    java.lang.Integer.class, java.lang.String.class,java.lang.String.class,java.lang.String.class, java.lang.Integer.class, java.lang.Integer.class, java.lang.String.class
                };

                public Class getColumnClass(int columnIndex) {
                    return types [columnIndex];
                }
            });


        } catch (Exception e) {
        }
    }
    
              
