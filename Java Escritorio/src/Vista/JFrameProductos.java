    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        try {
            if (txtNombre.getText().trim().length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "DEBE INGRESAR NOMBRE!");
                txtNombre.requestFocus();
            } else if (txtPrecio.getText().trim().length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "DEBE INGRESAR PRECIO!");
                txtPrecio.requestFocus();
            } else if (txtStock.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "DEBE INGRESAR STOCK!");
                txtStock.requestFocus();
            } else if (txtFecha.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "DEBE INGRESAR FECHA CADUCIDAD!");
                txtFecha.requestFocus();
            } else {
                String nro_serie = txtNro.getText();
                String codigo = txtCodigo.getText();
                String nombre = txtNombre.getText();
                String descripcion = txtDescripcion.getText();
                int precio = Integer.parseInt(txtPrecio.getText());
                int stock = Integer.parseInt(txtStock.getText());
                String rut_empleado = txtRut.getText();
                int id_tipo_prod = Integer.parseInt(txtTipo.getText());
                int id_modelo = Integer.parseInt(txtModelo.getText());
                int id_sucursal = Integer.parseInt(txtSucursal.getText());
                int id_marca = Integer.parseInt(txtMarca.getText());
                int id_categoria = Integer.parseInt(txtCategoria.getText());
                int oferta = Integer.parseInt(txtOferta.getText());

                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                Date fecha_ingreso = sdf.parse(txtFecha.getText());
                //public Producto(String nro_serie, int precio, int stock, String codigo, String nombre, String descripcion, String rut_empleado, Date fecha_ingreso, int id_tipo_prod, int id_modelo, int id_sucursal, int id_orden_ped, int id_categoria, int id_marca)
                Producto p = new Producto(nro_serie,codigo, precio, stock,nombre, descripcion, oferta, fecha_ingreso,id_tipo_prod,id_modelo,id_sucursal, rut_empleado, id_categoria,id_marca);

                ProductoDao dao = new ProductoDao();
                boolean resp = dao.insertarProducto(p);
                if (resp) {
                    JOptionPane.showMessageDialog(rootPane, "Producto agregado");
                    cargarTabla();
                    limpiar();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Producto NO AGREGADO");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

