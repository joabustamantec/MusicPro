    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        try {
            int id_producto = id_producto_global;
            String nro_serie = txtNro.getText();
            String codigo = txtCodigo.getText();
            String nombre = txtNombre.getText();
            String descripcion = txtDescripcion.getText();
            int precio = Integer.parseInt(txtPrecio.getText());
            int stock = Integer.parseInt(txtStock.getText());
            String rut_empleado = txtRut.getText();
            int id_tipo_prod = Integer.parseInt(txtTipo.getText());
            int id_modelo = Integer.parseInt(txtModelo.getText());
            int id_sucursal = Integer.parseInt(txtSucursal.getText());
            int id_marca = Integer.parseInt(txtMarca.getText());
            int id_categoria = Integer.parseInt(txtCategoria.getText());
            int oferta = Integer.parseInt(txtOferta.getText());

            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            Date fecha_ingreso = sdf.parse(txtFecha.getText());

            Producto p = new Producto( id_producto,nro_serie, codigo, precio, stock,nombre, descripcion, oferta, fecha_ingreso,id_tipo_prod,id_modelo,id_sucursal, rut_empleado, id_categoria,id_marca);
            ProductoDao dao = new ProductoDao();
            boolean resp = dao.actualizar_producto(p);
            if (resp) {
                JOptionPane.showMessageDialog(rootPane, "PRODUCTO ACTUALIZADO!");
                limpiar();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(rootPane, "PRODUCTO NO SE ACTUALIZÓ!");
            }

        } catch (Exception e) {
        }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            if (jTable1.getSelectedRow() != -1) {
                int id_producto = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());

                ProductoDao dao = new ProductoDao();

                ResultSet rs = dao.obtener_producto(id_producto);
                while ( rs.next() ) {
                    txtNro.setText( rs.getString(2) );
                    txtCodigo.setText( rs.getString(3) );
                    txtNombre.setText( rs.getString(6) );
                    txtDescripcion.setText( rs.getString(7) );
                    txtPrecio.setText( rs.getInt(4)+"" );
                    txtStock.setText( rs.getInt(5)+"" );
                    txtRut.setText( rs.getString(13) );
                    txtTipo.setText( rs.getInt(10)+"" );
                    txtModelo.setText( rs.getInt(11)+"" );
                    txtSucursal.setText( rs.getInt(12)+"" );
                    txtMarca.setText( rs.getInt(15)+"" );
                    txtCategoria.setText( rs.getInt(14)+"" );
                    txtOferta.setText( rs.getInt(8)+"");
                    id_producto_global = rs.getInt(1);

                    //fecha se convierta a dd-MM-yyyy
                    txtFecha.setText( rs.getDate(9)+"" );

                }

            }else{
                JOptionPane.showMessageDialog(rootPane, "DEBE ELEGIR 1 PRODUCTO DE LA TABLA!");
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

