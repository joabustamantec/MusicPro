    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
                      try {
            if (txtRut.getText().trim().length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "DEBE INGRESAR NOMBRE!");
                txtNombre.requestFocus();
            } else if (txtContrasena.getText().trim().length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "DEBE INGRESAR PRECIO!");
                txtContrasena.requestFocus();
            } else if (txtPerfil.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "DEBE INGRESAR STOCK!");
                txtPerfil.requestFocus();
            } else {
                String rut_empleado = txtRut.getText();
                String nombre = txtNombre.getText();
                String ap_paterno = txtApellidoP.getText();
                String ap_materno = txtApellidoM.getText();
                int telefono = Integer.parseInt(txtTelefono.getText());                     
                String correo = txtCorreo.getText();
                String direccion = txtCorreo.getText();
                String usuario = txtUsuario.getText();
                String contrasena = txtContrasena.getText();
                int id_perfil = Integer.parseInt(txtPerfil.getText());
                //public Producto(String nro_serie, int precio, int stock, String codigo, String nombre, String descripcion, String rut_empleado, Date fecha_ingreso, int id_tipo_prod, int id_modelo, int id_sucursal, int id_orden_ped, int id_categoria, int id_marca) 
                Empleado e = new Empleado(rut_empleado,nombre,ap_paterno,ap_materno,telefono,correo,direccion,usuario,contrasena,id_perfil);

                EmpleadoDao dao = new EmpleadoDao();
                boolean resp = dao.insertarEmpleado(e);
                if (resp) {                    
                    JOptionPane.showMessageDialog(rootPane, "Empleado Agregado");
                    limpiar();
                    cargarTabla();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Empleado No Agregado");
                }
            }
        } catch (Exception p) {
            JOptionPane.showMessageDialog(rootPane, p.getMessage());
        }

    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void brnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            if (jTable1.getSelectedRow() != -1) {
                String rut_empleado = (jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());

                EmpleadoDao dao = new EmpleadoDao();

                ResultSet rs = dao.obtener_Empleado(rut_empleado);
                while ( rs.next() ) {
                    txtRut.setText( rs.getString(1) );
                    txtNombre.setText( rs.getString(2) );
                    txtApellidoP.setText( rs.getString(3) );
                    txtApellidoM.setText( rs.getString(4) );
                    txtTelefono.setText( rs.getInt(5)+"" );
                    txtCorreo.setText( rs.getString(6) );
                    txtDireccion.setText( rs.getString(7) );
                    txtUsuario.setText( rs.getString(8) );
                    txtContrasena.setText( rs.getString(9) );
                    txtPerfil.setText( rs.getInt(10)+"" );
         

                }

            }else{
                JOptionPane.showMessageDialog(rootPane, "DEBE ELEGIR 1 PRODUCTO DE LA TABLA!");
            }
        } catch (Exception e) {
        }

                                           
    }//GEN-LAST:event_brnBuscarActionPerformed

