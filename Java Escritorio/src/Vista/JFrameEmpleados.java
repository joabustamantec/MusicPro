    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() != -1) {
            String rut_empleado = (jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            EmpleadoDao dao= new EmpleadoDao();
            boolean resp = dao.eliminarEmpleado(rut_empleado);
            if (resp) {
                JOptionPane.showMessageDialog(rootPane, "EMPLEADO ELIMINADO!");
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(rootPane, "NO SE ELIMINO");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar una fila");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

