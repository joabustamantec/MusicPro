    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        // TODO add your handling code here:
        try {
            int id_orden_ped = id_pedido_global;

            String estado_orden = txtOrden.getText();
            
            String rut_cliente = txtCliente.getText();

            Pedido p = new Pedido(id_orden_ped,estado_orden, rut_cliente);
            ProductoDao dao = new ProductoDao();
            boolean resp = dao.actualizar_pedido(p);
            if (resp) {
                JOptionPane.showMessageDialog(rootPane, "ESTADO PEDIDO ACTUALIZADO!");
                limpiar();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(rootPane, "ESTADO PEDIDO NO SE ACTUALIZÓ!");
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnModificarActionPerformed

