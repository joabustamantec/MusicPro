    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String rut = this.txtCliente.getText();
        ClienteDao dao = new ClienteDao();
        ResultSet rs = dao.obtener_cliente(rut);
        

        DefaultTableModel tb_clientes = new DefaultTableModel();
        
        this.jTable2.setModel(tb_clientes);
        
        tb_clientes.setColumnIdentifiers(new Object[] {"RUT_CLIENTE", "NOMBRE", "APELLIDOS", "TELEFONO", "CORREO", "DIRECCION", "CONTRASENA"});
        
        try {
            while(rs.next()){
            
                tb_clientes.addRow(new Object[] {rs.getString("rut_cliente"),rs.getString("nombre"),rs.getString("apellidos"),rs.getInt("telefono"),rs.getString("correo"),rs.getString("direccion"),rs.getString("contrasena")});
                
                
                
            }
        } catch (Exception e) {
        }
                
              
     
    }//GEN-LAST:event_btnBuscarActionPerformed

