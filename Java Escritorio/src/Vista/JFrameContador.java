        public void cargarTabla(){
        try {
            //Rescatamos la información de todos los productos de la BD
            ProductoDao dao = new ProductoDao();
            ResultSet rs = dao.obtener_pedidos();

            //Cargar una lista con la información de la BD:
            List<Pedido> lista_pedidos = new ArrayList<>();
            while ( rs.next() ) {
                Pedido p = new Pedido();
                p.setId_orden_ped(rs.getInt(1) );
                p.setEstado_orden(rs.getString(3) );        
                p.setRut_cliente(rs.getString(7) );  
                lista_pedidos.add(p);
            }

            //cargar la tabla:
            String[][] arreglo = new String[lista_pedidos.size()][6];
            for (int i = 0; i < lista_pedidos.size(); i++) {
                arreglo[i][0] = lista_pedidos.get(i).getId_orden_ped()+"";
                arreglo[i][1] = lista_pedidos.get(i).getEstado_orden();
                arreglo[i][2] = lista_pedidos.get(i).getRut_cliente();               

            }
            jTable1.setModel(new javax.swing.table.DefaultTableModel(
                arreglo,
                new String [] {
                    "ID_ORDEN_PED", "ESTADO_ORDEN", "RUT CLIENTE"
                }
            ) {
                Class[] types = new Class [] {
                    java.lang.Integer.class, java.lang.String.class,java.lang.String.class
                };

                public Class getColumnClass(int columnIndex) {
                    return types [columnIndex];
                }
            });

        } catch (Exception e) {
        }
    }
                                    
    
