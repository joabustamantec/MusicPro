    private void btnRellenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRellenarActionPerformed
        // TODO add your handling code here:
        try {
            if (jTable1.getSelectedRow() != -1) {
                int id_orden_ped = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());

                ProductoDao dao = new ProductoDao();

                ResultSet rs = dao.obtener_pedido(id_orden_ped);
                while ( rs.next() ) {
                    
                    id_pedido_global = rs.getInt(1);
                    
                    txtOrden.setText( rs.getString(3) );
                    txtCliente.setText(rs.getString (7));

                    

                    //fecha se convierta a dd-MM-yyyy

                }

            }else{
                JOptionPane.showMessageDialog(rootPane, "SELECCIONAR UN PEDIDO DE LA TABLA!");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnRellenarActionPerformed

